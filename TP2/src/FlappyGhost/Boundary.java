package FlappyGhost;

public class Boundary {

    public double x;
    public double y;
    public double width;
    public double height;

    public Boundary(double x, double y, double width, double height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public boolean overlaps(Boundary other) {
        boolean isOverlapped = false;

        return isOverlapped;

    }
}
